void HAL_ADC_ConvCpltCallback( ADC_HandleTypeDef * hadc){
	if(hadc->Instance == hadc3.Instance){
		ADC3_flag = 1;
		if(BAT_GetLevelPercent(&hadc3) < 10){
			GoToSleep();
		}
	}

	if(hadc->Instance == hadc1.Instance){
		ADC1_flag = 1;
	}

	MIC_AddToMeanLevel(microphone);
	MIC_AddToAverage(microphone);
	mic_averaged = MIC_GetAverage();
	mic_mean_level = MIC_GetMeanLevel();

	speaker_output[0] = SPKR_CalculateOutput(mic_averaged, mic_mean_level, sound_level_control);

	HAL_DAC_Start(&hdac1, DAC_CHANNEL_1);
	HAL_DAC_Start_DMA(&hdac1, DAC_CHANNEL_1, (uint32_t *) speaker_output, 1, DAC_ALIGN_12B_R);
}

void HAL_DAC_ConvCpltCallbackCh1( DAC_HandleTypeDef * hdac){
	DAC_flag = 1;

	//Pause DAC to minimize interruption
	HAL_DAC_Stop_DMA(&hdac1, DAC_CHANNEL_1);
	HAL_ADC_Start_DMA(&hadc1, &microphone, 1);
}

void HAL_TIM_PeriodElapsedCallback( TIM_HandleTypeDef * htim){
	if(htim->Instance == htim3.Instance) {
		Buttons_actions = SYS_HandleButtons(Buttons_states);

		if(Buttons_actions.Up == SHORT) sound_level_control += 0.2;
		if(Buttons_actions.Down == SHORT) sound_level_control -= 0.2;
		if(Buttons_actions.Main == LONG) {
			switch(system_state){
			case ON: // Sleep the headphones
				GoToSleep();
				break;

			case OFF: // Wake up the headphones
				WakeUp();
				break;
			}
		}
	}
}

